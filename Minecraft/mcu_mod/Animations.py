from ursina import *

class Animations(self, Objects, Durations):
  def animate(self, EaseInOut, PosX, PosY):
    self.objects = Objects
    self.duration = Duration
    self.easeinout = EaseInOut
    self.x = PosX
    self.y = PosY

Animations.animate(
  self,
  EaseInOut = 'None',
  PosX = 0,
  PosY = 0
)
    
